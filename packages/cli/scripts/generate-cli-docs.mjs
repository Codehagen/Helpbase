#!/usr/bin/env node
/**
 * Walk the commander program and emit a single markdown reference page.
 *
 * Usage:
 *   pnpm --filter helpbase docs:cli              # regenerate docs/cli.md
 *   pnpm --filter helpbase docs:cli --check      # fail if docs are stale (CI)
 *
 * Keeps docs/cli.md authoritative by regenerating from commander metadata.
 * To hand-edit examples, add them via .addHelpText('after', ...) on the
 * command definition — they flow through into this file.
 */
import fs from "node:fs"
import path from "node:path"
import { fileURLToPath } from "node:url"

const here = path.dirname(fileURLToPath(import.meta.url))
const REPO_ROOT = path.resolve(here, "../../..")
const OUTPUT = path.join(REPO_ROOT, "docs/cli.md")
const CHECK = process.argv.includes("--check")

// Import the built CLI module so commander is populated. Import side-effects
// call program.parse(), so we swap argv to --help first and capture output
// via a child process instead.
import { execSync } from "node:child_process"

const CLI = path.resolve(here, "../dist/index.js")
if (!fs.existsSync(CLI)) {
  console.error("Build the CLI first: pnpm --filter helpbase build")
  process.exit(1)
}

function help(args) {
  try {
    return execSync(`node ${CLI} ${args}`, {
      encoding: "utf-8",
      env: { ...process.env, NO_COLOR: "1", NO_UPDATE_NOTIFIER: "1" },
    })
  } catch (err) {
    // commander exits 1 on --help from a subcommand that exits with usage
    // shown; stdout still has the content.
    return (err.stdout ?? "") + (err.stderr ?? "")
  }
}

const topHelp = help("--help")

// Extract commands from the top-level help output — commander lists them in
// a "Commands:" block that runs to end of output.
const commands = []
{
  const idx = topHelp.indexOf("Commands:")
  if (idx !== -1) {
    const block = topHelp.slice(idx)
    for (const line of block.split("\n")) {
      // "  dev [options]       Start the development server"
      const m = line.match(/^ {2}(\w[\w-]*)(?:\s|$)/)
      if (m && m[1] !== "help" && !commands.includes(m[1])) {
        commands.push(m[1])
      }
    }
  }
}

const version = (() => {
  try {
    const pkg = JSON.parse(
      fs.readFileSync(path.resolve(here, "../package.json"), "utf-8"),
    )
    return pkg.version
  } catch {
    return "unknown"
  }
})()

const lines = []
lines.push("<!-- Generated by packages/cli/scripts/generate-cli-docs.mjs -->")
lines.push("<!-- Do not edit by hand. Run `pnpm --filter helpbase docs:cli`. -->")
lines.push("")
lines.push(`# helpbase CLI reference`)
lines.push("")
lines.push(
  `> Generated from commander metadata. CLI version: \`${version}\`.`,
)
lines.push("")
lines.push("## Commands")
lines.push("")
for (const cmd of commands) {
  lines.push(`- [\`helpbase ${cmd}\`](#helpbase-${cmd})`)
}
lines.push("")

for (const cmd of commands) {
  lines.push(`## \`helpbase ${cmd}\``)
  lines.push("")
  lines.push("```")
  lines.push(help(`${cmd} --help`).trim())
  lines.push("```")
  lines.push("")
}

const output = lines.join("\n") + "\n"

if (CHECK) {
  const existing = fs.existsSync(OUTPUT) ? fs.readFileSync(OUTPUT, "utf-8") : ""
  if (existing !== output) {
    console.error(
      "docs/cli.md is stale. Run `pnpm --filter helpbase docs:cli` to regenerate.",
    )
    process.exit(1)
  }
  console.log("docs/cli.md is up to date.")
} else {
  fs.mkdirSync(path.dirname(OUTPUT), { recursive: true })
  fs.writeFileSync(OUTPUT, output)
  console.log(
    `Wrote ${path.relative(REPO_ROOT, OUTPUT)} (${commands.length} commands)`,
  )
}
